package Lab;

public class AverageCalculations {
public static void main(String[] args) {
	int a = 10;
	int b = 20;
	int c = 30;
	int d = 40;
	int e = 50;
	int f = (a+b+c+d+e); //calculating the sum of a,b,c,d,e
	int g = (f/5); // calculating the Average of a,b,c,d,e
	System.out.println("Average of five no. is :" + g);
}
}
